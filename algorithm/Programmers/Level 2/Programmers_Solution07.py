# 실행 결과
# 실행 결과가 여기에 표시됩니다.
def solution(s):
    answer = 0
    for i in range(len(s)):
        stack = []
        cnt = 0
        for i in range(len(s)):
            if s[i] == '[' or s[i] == '(' or s[i] == '{':
                 stack.append(s[i])
                 cnt+=1
            elif s[i] == ']' or s[i] == ')' or s[i] == '}':
                if len(stack)!=0:
                    if s[i] ==']':
                        if stack[-1] == '[':
                           stack.pop()
                    elif s[i] == ')':
                        if stack[-1] == '(':
                            stack.pop()
                    elif s[i] == '}':
                        if stack[-1] == '{':
                            stack.pop()
        if len(stack)==0 and cnt !=0: answer+=1
        s = s[1:]+s[0]

    return answer

print(solution("}}}"))
